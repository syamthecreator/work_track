class ProfileModel {
  final String name;
  final String id;
  final String email;
  final String gender;
  final String phone;
  final String profileImage;
  final String address;
  final String status;
  final String memberSince;
  final String language;
  final String role;

  ProfileModel({
    required this.name,
    required this.id,
    required this.email,
    required this.gender,
    required this.phone,
    required this.profileImage,
    required this.address,
    required this.status,
    required this.memberSince,
    required this.language,
    required this.role,
  });
}
